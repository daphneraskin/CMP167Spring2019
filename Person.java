public class Person {
	
	String name = "";
	int age;
	char gender;
	//special type of method; it is caled a "constructor". 
	//Constructors have the same name as the class
	public Person(String name, int age, char gender) {
		this.name = name;
		this.age = age;
		this.gender = gender;
		
		
	}
}
